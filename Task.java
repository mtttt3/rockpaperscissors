public class Task {

    public String name;
    public int timeReq;
    public String person;
    public String deadline;
    public String priority;

    Task(String name, int timeReq, String person, String deadline, String priority)
    {
        this.name = name;
        this.timeReq = timeReq;
        this.person = person;
        this.deadline = deadline;
        this.priority = priority;
        
    }
}
